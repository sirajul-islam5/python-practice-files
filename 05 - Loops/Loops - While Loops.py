# print numbers from 1 to 5 using while loop
i = 1
while i <= 5:
    print(i)
    i += 1

print("loop ended")

# print numbers from 1 to 5 using while loop but in reverse way
i = 5
while i >= 1:
    print(i)
    i -= 1

print("loop ended")